package com.test.Product;

public class Customer {
	int id;
	String name;
	double billEle;
	double billCloth;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getBillEle() {
		return billEle;
	}
	public void setBillEle(double billEle) {
		this.billEle = billEle;
	}
	public double getBillCloth() {
		return billCloth;
	}
	public void setBillCloth(double billCloth) {
		this.billCloth = billCloth;
	}

}
